    public void ConfirmPay() // if loan_id have fine then pay
    {
        try{
            Connection con = SQLConnection.getConnection();
            String sql = "UPDATE fines SET paid=? WHERE loan_id='"+jTextField1.getText()+"'";
            String sql1 = "UPDATE book_loans SET date_in=? WHERE loan_id='"+jTextField1.getText()+"'";
            
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, "true");
            
            LocalDate todayDate = LocalDate.now();
            DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd");
            String datetoday = dtf.format(todayDate);
            
            PreparedStatement pst1 = con.prepareStatement(sql1);
            pst1.setString(1, datetoday);
            
            if(pst.executeUpdate() > 0 && pst1.executeUpdate() > 0)
            {
                NotificationShow ns = new NotificationShow();
                ns.setVisible(true);
                ns.jLabel2.setText("Payment Successfull");
                this.dispose();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error Query:"+e);
        }
    }
