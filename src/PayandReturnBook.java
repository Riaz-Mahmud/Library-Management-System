    public void CheckMoney() //Check loan_id have any fine or not
    {
        if(jTextField1.getText().trim().isEmpty() )
        {
            if(jTextField1.getText().trim().isEmpty())
            {
                jTextField1.requestFocus(true);
            }
        }
        else
        {
            String status ="false";
            try
            {
                Connection con = SQLConnection.getConnection();
                String sql = "Select fine_amt From fines Where loan_id='"+jTextField1.getText()+"' AND paid='"+status+"'";
                PreparedStatement pst = con.prepareStatement(sql);
                ResultSet rs = pst.executeQuery();
                if(rs.next())
                {
                    jLabel6.setText("$ "+rs.getDouble(1));
                    jPanel3.setBackground(new Color(255,0,0));
                    jButton2.setEnabled(true);
                }
                else
                {
                    jButton2.setEnabled(false);
                    jPanel3.setBackground(new Color(51,204,0));
                    jLabel6.setText("$ 0.0");
                }
            con.close();
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }
