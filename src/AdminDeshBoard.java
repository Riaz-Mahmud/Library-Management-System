    public void DeleteBook() //Delete book
    {
        String Bookisbn = isbnTxt1.getText();
        String BookAuthorID = authorID1.getText();
        try
        {
            Connection con = SQLConnection.getConnection();
            String sql = "DELETE FROM book WHERE isbn='"+Bookisbn+"'";//AND ab.author_id='"+BookAuthorID+"' AND a.author_id='"+BookAuthorID+"'
            PreparedStatement pst = con.prepareStatement(sql);

            if(pst.executeUpdate() > 0)
            {
                try{
                    String sql1 = "DELETE FROM book_authors WHERE author_id='"+BookAuthorID+"'";
                    PreparedStatement pst1 = con.prepareStatement(sql1);
                    if(pst1.executeUpdate() > 0)
                    {
                        try
                        {
                            String sql2 = "DELETE FROM authors WHERE author_id='"+BookAuthorID+"'";
                            PreparedStatement pst2 = con.prepareStatement(sql2);
                            if(pst2.executeUpdate() > 0)
                            {
                                ShowBookList();
                                NotificationShow ns = new NotificationShow();
                                ns.setVisible(true);
                                ns.jLabel2.setText("Delete Successfull");
                                isbnTxt1.setText("");
                                title1.setText("");
                                authorID1.setText("");
                                name1.setText("");
                            }
                        }catch(Exception e){
                            JOptionPane.showMessageDialog(null,"Error 3st Query:"+ e);
                        }
                    }  
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null,"Error 2st Query:"+ e);
                }
            }
        con.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Error 1st Query:"+e);
        }
    }
