    public void Submit() // add new book
    {
        if(isbnTxt.getText().trim().isEmpty() || title.getText().trim().isEmpty() || 
                authorID.getText().trim().isEmpty() || name.getText().trim().isEmpty()  )
        {
            if(isbnTxt.getText().trim().isEmpty())
            {
                isbn_lable.setText("Empty");
            }
            if(title.getText().trim().isEmpty())
            {
                title_lable.setText("Empty");
            }
            if(authorID.getText().trim().isEmpty())
            {
                jLabel54.setText("Empty");
            }
            
            if(name.getText().trim().isEmpty())
            {
                jLabel56.setText("Empty");
            }
        }
        else
        {
            try
            {
                Connection con = SQLConnection.getConnection();
                String sql = "INSERT INTO book (isbn,title) VALUES (?, ?)";
                PreparedStatement pst = con.prepareStatement(sql);
                pst.setString(1, isbnTxt.getText());
                pst.setString(2, title.getText());
                
                String sql1 = "INSERT INTO book_authors (author_id,isbn) VALUES (?, ?)";
                PreparedStatement pst1 = con.prepareStatement(sql1);
                pst1.setString(1, authorID.getText());
                pst1.setString(2, isbnTxt.getText());
                
                String sql2 = "INSERT INTO authors (author_id,name) VALUES (?, ?)";
                PreparedStatement pst2 = con.prepareStatement(sql2);
                pst2.setString(1, authorID.getText());
                pst2.setString(2, name.getText());
                
                if(pst.executeUpdate() > 0 && pst1.executeUpdate() > 0 && pst2.executeUpdate() > 0)
                {
                    isbnTxt.setText("");
                    title.setText("");
                    authorID.setText("");
                    name.setText("");
                    NotificationShow ns = new NotificationShow(); //Call Notification to show New Notification
                    ns.setVisible(true);
                    ns.jLabel2.setText("Book Added Successfully");
                }
            con.close();
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,"Add Book 1st Query Error: "+ e);
            }
        }
    }
