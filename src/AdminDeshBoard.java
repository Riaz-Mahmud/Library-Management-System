    private int nextISBN = 0;
    public void generate_isbn()  //Create automatic ISBN, its follow the last isbn
    {
        try
        {
            Connection con = SQLConnection.getConnection();
            Statement st = con.createStatement();
            String query = "SELECT isbn FROM book ORDER BY isbn DESC LIMIT 1";
            ResultSet rs = st.executeQuery(query);
            if(rs.next())
            {
                this.ISBN = rs.getInt(1);
                this.nextISBN = ISBN+1;
                isbnTxt.setText(String.valueOf(nextISBN));
            }
            else
            {
                nextISBN = 1111111111;
                isbnTxt.setText(String.valueOf(nextISBN));
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }
