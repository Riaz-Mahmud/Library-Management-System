    public void SearchUserList()  //Search book form Search in admin panel
    {
        String serch = jTextField5.getText();
        try 
        {
            Connection con = SQLConnection.getConnection();
            String query = "SELECT b.isbn, b.title, a.name FROM book b,book_authors ba, authors a WHERE b.isbn = ba.isbn AND ba.author_id = a.author_id AND b.title LIKE '%"+serch+"%' OR a.name LIKE '%"+serch+"%' OR b.isbn LIKE '%"+serch+"%' ORDER BY isbn DESC LIMIT 1";
            PreparedStatement ps = con.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            if(rs.next())
            {
                String bookisbn = rs.getString(1);
                jLabel70.setText(rs.getString(1));
                jLabel69.setText(rs.getString(2));
                jLabel68.setText(rs.getString(3));
                jLabel67.setText("NUll");
                try{
                    String query1 = "SELECT isbn FROM book_loans WHERE isbn='"+bookisbn+"' ORDER BY isbn DESC LIMIT 1";
                    PreparedStatement ps1 = con.prepareStatement(query1);
                    ResultSet rs1 = ps1.executeQuery();
                    if(rs1.next())
                    {
                        jLabel67.setText("checked out");
                    }
                    else{
                        jLabel67.setText("checked in");
                    }
                }
                catch (Exception e) 
                {
                    JOptionPane.showMessageDialog(null,"Error in Book availability found: \n"+e);
                }
            }
            else 
            {
                jLabel67.setText("");
                jLabel68.setText("");
                jLabel69.setText("");
                jLabel70.setText("");
                NotificationShow ns = new NotificationShow();
                ns.setVisible(true);
                ns.jLabel2.setText("Book not Found!");
                ns.jButton1.setText("Ok");
            }
            con.close();
        } 
        catch (Exception e) 
        {
            JOptionPane.showMessageDialog(null,"Error: "+e);
        }
    }
