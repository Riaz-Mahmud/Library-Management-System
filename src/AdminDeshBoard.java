    public void UpdateBook() //Update book
    {
        String Bookisbn = isbnTxt1.getText();
        String BookAuthorID = authorID1.getText();
        try{
            Connection con = SQLConnection.getConnection();
            String sql = "UPDATE book SET isbn=?, title=? WHERE isbn='"+Tablebookisbn+"'";
            String sql1 = "UPDATE authors SET author_id=?, name=? WHERE author_id='"+TablebookAuthorid+"'";
            String sql2 = "UPDATE book_authors SET author_id=?, isbn=? WHERE author_id='"+TablebookAuthorid+"'";

            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, isbnTxt1.getText());
            pst.setString(2, title1.getText());
            
            PreparedStatement pst1 = con.prepareStatement(sql1);
            pst1.setString(1, authorID1.getText());
            pst1.setString(2, name1.getText());
            
            PreparedStatement pst2 = con.prepareStatement(sql2);
            pst2.setString(1, authorID1.getText());
            pst2.setString(2, isbnTxt1.getText());
            if(pst.executeUpdate() > 0 && pst1.executeUpdate() > 0 && pst2.executeUpdate() > 0)
            {
                ShowBookList();
                NotificationShow ns = new NotificationShow();
                ns.setVisible(true);
                ns.jLabel2.setText("Update Successfull");
                isbnTxt1.setText("");
                title1.setText("");
                authorID1.setText("");
                name1.setText("");     
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error Query:"+e);
        }
    }
